// Helper function to get holidays by month
export const getHolidaysByMonth = (month) => {
  return indonesiaHolidays.filter(h => {
    const date = new Date(h.date);
    return date.getMonth() === month;
  });
};

// Helper function to get holidays count by category
export const getHolidaysCount = () => {
  return {
    total: indonesiaHolidays.length,
    public: indonesiaHolidays.filter(h => h.category === 'public').length,
    joint: indonesiaHolidays.filter(h => h.category === 'joint').length
  };
};

